



// Difference Between findOne() and find()



/*

>>>> findOne() :  --- It is a method used in MongoDB to query a collection and return a single document
                      that matches the specified query criteria. 
                  --- It does not return an array, but rather a single document that is the first document
                      to match the query. 
                      

>>>> find() :  --- This method returns an array of all documents that match the specified query criteria.

*/