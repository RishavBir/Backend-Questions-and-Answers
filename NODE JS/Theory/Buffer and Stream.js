




// What is Nodejs Buffer and Stream ??


/*

    >>>>>  BUFFER : --- Store binary data in memory.
                    --- Fixed-size allocation.
                    --- Read and write binary data.
                    --- Data is stored in memory.
                


    >>>>   STREAM : --- Process data as it's read or written, chunk by chunk.
                    --- Size can vary depending on the data being read or written.
                    --- Read and write data as chunks.
                    --- Read and write data to minimize memory usage. 

*/